package aggrigation;

public class Address {
	
	String street, city, country, postalCode, telNumber;

	public Address(String street, String city, String country, String postalCode, String telNumber) {
		super();
		this.street = street;
		this.city = city;
		this.country = country;
		this.postalCode = postalCode;
		this.telNumber = telNumber;
	}
	
}
